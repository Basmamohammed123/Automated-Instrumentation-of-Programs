def add(a):
    """ Add a to the current number. """
    if a > 5:
        return a + 2
    else:
        return a + 4

def multiply(a, lst):
    """ Multiplies the given number a by the given list. """
    while a > 0:
        for i in lst:
            i + a
    return lst

def divide(a, b):
    """ Divide two numbers """
    return a / b